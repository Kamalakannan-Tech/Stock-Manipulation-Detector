@bp.route('/tickers', methods=['GET'])
def list_tickers():
    """GET /api/tickers — list all monitored tickers with quick status. Auto-generates alerts."""
    result = []
    for ticker in TICKERS:
        t = ticker.strip()
        entry = {'ticker': t, 'status': 'active'}
        pred = _run_live_prediction(t)
        if pred:
            pred['ticker'] = t
            entry.update({
                'manipulation_probability': pred.get('manipulation_probability', 0),
                'risk_level':               pred.get('risk_level', 'UNKNOWN'),
                'latest_price':             pred.get('latest_price'),
            })
            _maybe_generate_alert(pred)  # auto-write alert if above threshold
        result.append(entry)
    return jsonify({'tickers': result, 'count': len(result),
                    'timestamp': datetime.now().isoformat()})


@bp.route('/ticker/<string:symbol>/status', methods=['GET'])
def ticker_status(symbol):
    """GET /api/ticker/<sym>/status — current live metrics and prediction for one ticker."""
    symbol = symbol.upper().strip()
    pred   = _run_live_prediction(symbol)

    # Use live yfinance data (has RSI/MACD/Vol-Z computed) → fall back to raw CSV
    rows = _fetch_live_ohlcv(symbol, n=200) or _load_raw_csv(symbol, n=200)

    if not rows and not pred:
        return jsonify({'error': f'No data available for {symbol}'}), 404

    latest = rows[-1] if rows else {}
    return jsonify({
        'ticker':    symbol,
        'timestamp': datetime.now().isoformat(),
        'market': {
            'latest_price': round(float(latest.get('Close') or latest.get('close') or 0), 4),
            'volume':       int(latest.get('Volume') or latest.get('volume') or 0),
            'returns_1h':   round(float(latest.get('Returns_1h') or latest.get('returns_1h') or 0), 6),
            'rsi':          round(float(latest.get('RSI') or latest.get('rsi') or 50), 2),
            'macd':         round(float(latest.get('MACD') or latest.get('macd') or 0), 6),
            'abnormal_vol': round(float(latest.get('Abnormal_Volume_Zscore') or latest.get('vol_z') or 0), 4),
        },
        'prediction': pred,
    })


@bp.route('/ticker/<string:symbol>/chart', methods=['GET'])
def ticker_chart(symbol):
    """GET /api/ticker/<sym>/chart — live candles from yfinance, fallback to CSV."""
    symbol = symbol.upper().strip()
    n      = request.args.get('n', 100, type=int)
    # Try live data first, fall back to stored CSV
    rows = _fetch_live_ohlcv(symbol, n=n) or _load_raw_csv(symbol, n=n)
    if not rows:
        return jsonify({'error': f'No chart data for {symbol}'}), 404

    def _safe(val, default=0, decimals=4):
        """Convert to float safely, replacing NaN/inf with default."""
        try:
            f = float(val)
            return default if (f != f or f == float('inf') or f == float('-inf')) else round(f, decimals)
        except (TypeError, ValueError):
            return default

    def _fmt(row):
        ts = row.get('timestamp')
        return {
            'timestamp': ts.isoformat() if hasattr(ts, 'isoformat') else str(ts),
            'open':   _safe(row.get('Open'),   decimals=4),
            'high':   _safe(row.get('High'),   decimals=4),
            'low':    _safe(row.get('Low'),    decimals=4),
            'close':  _safe(row.get('Close'),  decimals=4),
            'volume': int(row.get('Volume') or 0),
            'rsi':    _safe(row.get('RSI',   50), default=50, decimals=2),
            'macd':   _safe(row.get('MACD',   0), decimals=6),
            'vol_z':  _safe(row.get('Abnormal_Volume_Zscore', 0), decimals=4),
        }

    return jsonify({
        'ticker': symbol,
        'candles': [_fmt(r) for r in rows],
        'count':   len(rows),
    })
